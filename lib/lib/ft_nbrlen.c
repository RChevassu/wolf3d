#include "../include/libft.h"

int	ft_nbrlen(int n)
{
	int	l;

	l = 1;
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n = -n;
		l++;
	}
	while (n > 9)
	{
		n = n / 10;
		l++;
	}
	return (l);
}
