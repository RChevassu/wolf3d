#include "../include/libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ret;
	int		i;

	i = 0;
	ret = (char*)s;
	while (s[i] != 0)
	{
		if (s[i] == (char)c)
			return ((char*)s + i);
		i++;
	}
	if (s[i] == (char)c)
		return ((char*)s + i);
	return (NULL);
}
